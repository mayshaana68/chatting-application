    private void sendMessage(){
        try {
            String msgout = "";
            msgout = msg_text.getText().trim();
            dout.writeUTF(msgout);
            msg_area.setText(msg_area.getText().trim() + "\nClient : " + msgout);
            msg_text.setText("");
        } catch (IOException ex) {
            ex.printStackTrace();
        }
    }
